package Capitulo4.Bloque2.Ejercicio3;

public class ComponenteDeArbol {
	private String nombreArbol;
	private String familiaArbol;
	
	
	/**
	 * 
	 */
	public ComponenteDeArbol() {
		super();
	}


	/**
	 * @param nombreArbol
	 * @param familiaArbol
	 */
	public ComponenteDeArbol(String nombreArbol, String familiaArbol) {
		super();
		this.nombreArbol = nombreArbol;
		this.familiaArbol = familiaArbol;
	}


	public String getNombreArbol() {
		return nombreArbol;
	}


	public void setNombreArbol(String nombreArbol) {
		this.nombreArbol = nombreArbol;
	}


	public String getFamiliaArbol() {
		return familiaArbol;
	}


	public void setFamiliaArbol(String familiaArbol) {
		this.familiaArbol = familiaArbol;
	}
	
	
	
	
	

}
